  static ProductModel _product({
    required String id,
    required String title,
    required double price,
    required String size,
    required String seller,
    required String sellerName,
    required ProductCategory category,
    SellingType sellingType = SellingType.fixedPrice,
    double? currentBid,
    double? startingBid,
    int bidCount = 0,
    DateTime? bidEnd,
    bool buyNow = false,
    String? brand,
    String? color,
  }) {
    final sellerInfo = sellers.firstWhere((s) => s.username == seller);
    return ProductModel(
      id: id,
      sellerUsername: seller,
      sellerName: sellerName,
      sellerAvatar: sellerInfo.avatarUrl,
      sellerVerified: sellerInfo.isVerified,
      title: title,
      description:
          'Pre-loved $title in great condition. Carefully curated and ready for a new home. Message seller for more details or measurements.',
      price: price,
      category: category,
      condition: ProductCondition.good,
      imageUrls: ['https://picsum.photos/seed/$id/400/400'],
      size: size,
      brand: brand,
      color: color,
      material: 'Mixed',
      location: sellerInfo.location,
      createdAt: _now.subtract(Duration(days: int.parse(id.split('_').last) % 10)),
      viewCount: 50 + int.parse(id.split('_').last) * 12,
      favoriteCount: 5 + int.parse(id.split('_').last),
      sellingType: sellingType,
      currentBid: currentBid,
      startingBid: startingBid ?? price,
      bidCount: bidCount,
      bidEndTime: bidEnd,
      buyNowEnabled: buyNow || sellingType == SellingType.fixedPrice,
      distanceKm: sellerInfo.distanceKm,
      likesCount: 3 + int.parse(id.split('_').last),
      bidHistory: currentBid != null
          ? [
              BidEntry(
                id: 'bh_${id}_1',
                username: 'user***42',
                amount: (startingBid ?? price),
                createdAt: _now.subtract(const Duration(hours: 5)),
              ),
              BidEntry(
                id: 'bh_${id}_2',
                username: 'user***17',
                amount: (startingBid ?? price) + 30,
                createdAt: _now.subtract(const Duration(hours: 3)),
              ),
              BidEntry(
                id: 'bh_${id}_3',
                username: 'user***89',
                amount: currentBid,
                createdAt: _now.subtract(const Duration(hours: 1)),
              ),
            ]
          : [],
    );
  }

